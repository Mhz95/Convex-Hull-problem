package ConvexHull_Project;

/**
 *
 * @author Shams
 */
public class Point {
    
    public float x;
    public float y;
    
    public Point ( float x1 , float y1)
    {
        x = x1;
        y = y1;
    }
    
    public float getX ()
    {
        return x;
    }
    
    public float getY()
    {
        return y;
    }    
}
